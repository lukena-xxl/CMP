<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass="App\Repository\OrderProductRepository")
 */
class OrderProduct
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $name;

    /**
     * @ORM\Column(type="integer")
     */
    private $quantity;

    /**
     * @ORM\Column(type="integer")
     */
    private $price;

    /**
     * @ORM\Column(type="string", length=255, nullable=true)
     */
    private $image;

    /**
     * @ORM\ManyToOne(targetEntity="App\Entity\ProductItem")
     */
    private $product;

    /**
     * @ORM\ManyToOne(targetEntity="App\Entity\Orders", inversedBy="products")
     * @ORM\JoinColumn(name="in_order_id", referencedColumnName="id", onDelete="CASCADE")
     */
    private $in_order;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }

    public function getQuantity(): ?int
    {
        return $this->quantity;
    }

    public function setQuantity(int $quantity): self
    {
        $this->quantity = $quantity;

        return $this;
    }

    public function getPrice(): ?int
    {
        return $this->price;
    }

    public function setPrice(int $price): self
    {
        $this->price = $price;

        return $this;
    }

    public function getImage(): ?string
    {
        return $this->image;
    }

    public function setImage(?string $image): self
    {
        $this->image = $image;

        return $this;
    }

    public function getProduct(): ?ProductItem
    {
        return $this->product;
    }

    public function setProduct(?ProductItem $product): self
    {
        $this->product = $product;

        return $this;
    }

    public function getInOrder(): ?Orders
    {
        return $this->in_order;
    }

    public function setInOrder(?Orders $in_order): self
    {
        $this->in_order = $in_order;

        return $this;
    }
}
